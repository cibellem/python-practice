# if / else statements #

age = int(input("How old are you?"))
if age >= 50:
    print("You need to take the new vaccine")
else:
    print("You don't need to take the new vaccine")


# Even / Odd Number #

number = int(input("Enter a number:"))
if number / 2 == 0:
    print("This is a even number")
else:
    print("This is an Odd number")
